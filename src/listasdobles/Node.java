package listasdobles;


public class Node {
    int value;
    Node next;
    Node prev;
    
    public Node(int value){
        this.value=value;
        this.next=null;
        this.prev=null;
        
    
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public Node getPrev() {
        return prev;
    }

    public void setPrev(Node prev) {
        this.prev = prev;
    }
    
    
    
    
}
